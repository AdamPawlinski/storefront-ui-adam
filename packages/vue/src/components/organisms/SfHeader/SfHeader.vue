<template>
  <!-- <div class="sf-header" > -->
  <!-- <div class="sf-header__wrapper" > -->
  <header
    ref="header"
    class="sf-header sf-header__wrapper sf-header__header"
    :class="{ 'is-sticky': sticky }"
  >
    <!--@slot Use this slot to replace logo with text or image-->
    <slot name="logo" v-bind="{ logo, title }">
      <SfLink link="/">
        <SfImage v-if="logo" :src="logo" :alt="title" class="sf-header__logo" />
        <h1 v-else class="sf-header__title">{{ title }}</h1>
      </SfLink>
    </slot>
    <div class="sf-header__aside">
      <!-- @slot Use this slot for language or currency selector -->
      <slot name="aside" />
    </div>
    <div class="sf-header__actions">
      <nav
        class="sf-header__navigation"
        :class="{ 'is-visible': isNavVisible }"
      >
        <slot name="navigation">
          <div v-if="!isVisibleOnMobile" class="sf-header__menu">
            <SfButton
              v-for="(item, i) in menuItems"
              :key="i"
              class="sf-header__menu-item sf-header__menu-item--desktop sf-button--pure"
              v-on="$listeners"
              @mouseenter="$emit('mouseenter', item)"
              @mouseleave="$emit('mouseleave', '')"
              @click="$emit('click', item)"
            >
              {{ item }}
            </SfButton>
          </div>
          <SfSidebar
            :visible="isVisibleOnMobile"
            :persistent="true"
            class="sf-header__menu--sidebar"
            @close="$emit('close')"
          >
            <SfMenuItem
              v-for="(item, i) in menuItems"
              :key="i"
              :label="item"
              class="sf-header__menu-item sf-header__menu-item--mobile"
            />
          </SfSidebar>
          <div class="sf-header__menu-content">
            <template v-for="item in menuItems">
              <slot :name="item" />
            </template>
          </div>
        </slot>
      </nav>
      <!--@slot Use this slot to replace default search bar-->
      <slot name="search" v-bind="{ searchValue, searchPlaceholder }">
        <SfSearchBar
          :value="searchValue"
          :placeholder="searchPlaceholder"
          aria-label="Search"
          class="sf-header__search"
          @input="$emit('change:search', $event)"
          @enter="$emit('enter:search', $event)"
        />
      </slot>
      <!--@slot Use this slot to replace default header icons with custom content-->
      <slot
        name="header-icons"
        v-bind="{
          activeIcon,
          cartHasProducts,
          cartItemsQty,
          cartIcon,
          wishlistIcon,
          accountIcon,
        }"
      >
        <div class="sf-header__icons">
          <SfButton
            v-if="accountIcon"
            class="sf-button--pure sf-header__action"
            data-testid="accountIcon"
            @click="$emit('click:account')"
          >
            <SfIcon
              :icon="accountIcon"
              size="1.25rem"
              :class="{
                'sf-header__icon is-active': activeIcon === 'account',
              }"
            />
          </SfButton>
          <SfButton
            v-if="wishlistIcon"
            class="sf-button--pure sf-header__action"
            data-testid="wishlistIcon"
            @click="$emit('click:wishlist')"
          >
            <SfIcon
              class="sf-header__icon"
              :icon="wishlistIcon"
              :has-badge="wishlistHasProducts"
              :badge-label="wishlistItemsQty"
              size="1.25rem"
              :class="{
                'sf-header__icon is-active': activeIcon === 'wishlist',
              }"
            />
          </SfButton>
          <SfButton
            v-if="cartIcon"
            class="sf-button--pure sf-header__action"
            data-testid="cartIcon"
            @click="$emit('click:cart')"
          >
            <SfIcon
              class="sf-header__icon"
              :icon="cartIcon"
              :has-badge="cartHasProducts"
              :badge-label="cartItemsQty"
              size="1.25rem"
              :class="{
                'sf-header__icon is-active': activeIcon === 'cart',
              }"
            />
          </SfButton>
        </div>
      </slot>
    </div>
  </header>
  <!-- </div> -->
  <!-- </div> -->
</template>
<script>
import Vue from "vue";

import SfHeaderNavigationItem from "./_internal/SfHeaderNavigationItem.vue";
Vue.component("SfHeaderNavigationItem", SfHeaderNavigationItem);
import {
  mapMobileObserver,
  unMapMobileObserver,
} from "../../../utilities/mobile-observer";
import { isClient } from "../../../utilities/helpers";
import SfImage from "../../atoms/SfImage/SfImage.vue";
import SfSearchBar from "../../molecules/SfSearchBar/SfSearchBar.vue";
import SfButton from "../../atoms/SfButton/SfButton.vue";
import SfIcon from "../../atoms/SfIcon/SfIcon.vue";
import SfLink from "../../atoms/SfLink/SfLink.vue";
import SfSidebar from "../../organisms/SfSidebar/SfSidebar";
import SfMenuItem from "../../molecules/SfMenuItem/SfMenuItem";

export default {
  name: "SfHeader",
  components: {
    SfImage,
    SfSearchBar,
    SfButton,
    SfIcon,
    SfLink,
    SfSidebar,
    SfMenuItem,
  },
  props: {
    /**
     * Header logo
     */
    logo: {
      type: [String, Object],
      default: "",
    },
    /**
     * Header title
     */
    title: {
      type: String,
      default: "",
    },
    /**
     * Navigation items
     */
    menuItems: {
      type: [String, Array],
      default: () => [],
    },
    /**
     * Header cartIcon (accepts same value as SfIcon)
     */
    cartIcon: {
      type: [String, Boolean, Array],
      default: "empty_cart",
    },
    /**
     * Header wishlistIcon (accepts same value as SfIcon)
     */
    wishlistIcon: {
      type: [String, Boolean, Array],
      default: "heart",
    },
    /**
     * Header accountIcon (accepts same value as SfIcon)
     */
    accountIcon: {
      type: [String, Boolean, Array],
      default: "profile",
    },
    /**
     * Header activeIcon (accepts account, wishlist and cart)
     */
    activeIcon: {
      type: String,
      default: "",
      validator(value) {
        return ["", "account", "wishlist", "cart"].includes(value);
      },
    },
    /**
     * Header search placeholder
     */
    searchPlaceholder: {
      type: String,
      default: "Search for items",
    },
    /**
     * Header search phrase
     */
    searchValue: {
      type: String,
      default: "",
    },
    /**
     * Header wishlist items quantity
     */
    wishlistItemsQty: {
      type: [String, Number],
      default: "0",
    },
    /**
     * Header cart items quantity
     */
    cartItemsQty: {
      type: [String, Number],
      default: "0",
    },
    /**
     * Header sticky to top
     */
    isSticky: {
      type: Boolean,
      default: false,
    },
    /**
     * Is nav slot visible on mobile view
     */
    isNavVisible: {
      type: Boolean,
      default: false,
    },
  },
  data() {
    return {
      icons: [],
      sticky: false,
      scrollDirection: null,
      lastScrollPosition: 0,
      animationStart: null,
      animationLong: null,
      animationDuration: 3000,
      currentItem: "",
    };
  },
  computed: {
    ...mapMobileObserver(),
    cartHasProducts() {
      return parseInt(this.cartItemsQty, 10) > 0;
    },
    wishlistHasProducts() {
      return parseInt(this.wishlistItemsQty, 10) > 0;
    },
    isVisibleOnMobile() {
      return this.isMobile;
    },
  },
  watch: {
    isSticky: {
      handler(isSticky) {
        if (!isClient) return;
        this.sticky = isSticky;
      },
      immediate: true,
    },
  },
  // methods: {
  //   clickHandler() {

  //   },
  //   mouseenterHandler() {

  //   },
  //   mouseleaveHandler() {

  //   },
  // },
  beforeDestroy() {
    unMapMobileObserver();
  },
};
</script>
